    public static FAIconType parseToFAIconType(String icon) {
        if (icon == null) {
            return null;
        }
        icon = icon.replace('-', '_');
        try {
            return FAIconType.valueOf(icon);
        } catch (IllegalArgumentException e) {
            return null;
        }
    }

